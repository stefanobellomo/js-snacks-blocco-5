const names = ['Edoardo', 'Simone', 'Francesco'];

// Stampa in console tutti i nomi
let name = []
names.forEach(thisame => {
    name.push(thisame);
})
console.log(name);

// Risultato: 'Edoardo', 'Simone', 'Francesco'
let naem = []
for (let i = 0; i < names.length; i++) {
    const name = names[i];
    naem.push(name)
}
console.log(naem);